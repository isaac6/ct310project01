<h1>International Peace Garden</h1>
<p> The International Peace Garden is a 3.65-square-mile park located adjacent to the International Peace Garden Border Crossing between Canada and the United States, in the state of North Dakota and the province of Manitoba. It was established on July 14, 1932, as a symbol of the peaceful relationship between the two nations.
</p>
<p>
The park plants over 150,000 flowers each year. Main features of the garden include an 18-foot floral clock display, fountains, a chime, and twin 120-foot concrete towers straddling the border with a peace chapel at their base.
</p>
